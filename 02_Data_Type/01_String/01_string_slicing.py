a = "I am good"
print(a)
#  I am good -> Sting ven by the user.
#  012345678 -> String counted by the Python.
#-(987654321)-> String counted by the Python but the number are negative.


# The Third bracket after a indicates the number of string (a) must be printed.
print(a[4])

# The third bracket and the semicolumn both determines the range of character that must be printed.
print(a[0:4])

# If we want to skip any character then we have set it by adding semicolon and input no of character you want to skip .
print(a[0:9:2])

# If we don't give any range then the range has been set by default full line .
print(a[:])

